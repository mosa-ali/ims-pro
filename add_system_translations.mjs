// Find where to insert EN translations
const enMatch = content.match(/export const en: Translations = \{[\s\S]*?\n\};/);
if (!enMatch) {
  console.error('Could not find EN translations object');
  process.exit(1);
}

const enInsertPoint = enMatch[0].lastIndexOf('};');
const enNewContent = content.slice(0, enMatch.index + enInsertPoint) + 
                     enSystemTranslations + '\n' +
                     content.slice(enMatch.index + enInsertPoint);

// Find where to insert AR translations
const arMatch = enNewContent.match(/export const ar: Translations = \{[\s\S]*?\n\};/);
if (!arMatch) {
  console.error('Could not find AR translations object');
  process.exit(1);
}

const arInsertPoint = arMatch[0].lastIndexOf('};');
const finalContent = enNewContent.slice(0, arMatch.index + arInsertPoint) + 
                     arSystemTranslations + '\n' +
                     enNewContent.slice(arMatch.index + arInsertPoint);

fs.writeFileSync(translationsPath, finalContent);
console.log('System translations added successfully');
