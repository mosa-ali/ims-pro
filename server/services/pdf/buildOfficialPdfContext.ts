/**
 * Validate official PDF context
 */
export function validateOfficialPdfContext(context: OfficialPdfContext): void {
  const requiredFields: (keyof OfficialPdfContext)[] = [
    'organizationId',
    'organizationName',
    'operatingUnitId',
    'operatingUnitName',
    'language',
    'direction',
    'generatedBy',
    'generatedAt',
    'documentType',
    'documentId',
  ];

  for (const field of requiredFields) {
    if (!context[field]) {
      throw new Error(`Missing required field in official PDF context: ${String(field)}`);
    }
  }

  console.log(`[Official PDF Context] ✅ Validation passed`);
}
