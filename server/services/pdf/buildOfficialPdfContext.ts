/**
 * Get HTML footer with page numbers and metadata
 */
export function getOfficialPdfFooter(
  context: OfficialPdfContext,
  pageNumber: number = 1,
  totalPages: number = 1
): string {
  const pageText =
    context.language === 'ar'
      ? `${context.labels.page} ${pageNumber} ${context.labels.of} ${totalPages}`
      : `${context.labels.page} ${pageNumber} ${context.labels.of} ${totalPages}`;

  const isRTL = context.isRTL;

  return `
    <div class="footer" style="direction: ${isRTL ? 'rtl' : 'ltr'};">
      <div class="footer-left" style="text-align: ${isRTL ? 'right' : 'left'};">
        <div>${context.footerOrganizationName}</div>
        <div>${context.footerOperatingUnitName}</div>
        <div style="margin-top: 5px; font-size: 9px; color: #999;">
          ${context.labels.generatedBy}: ${context.generatedBy}
        </div>
      </div>
      <div class="footer-right" style="text-align: ${isRTL ? 'left' : 'right'};">
        <div>${context.footerDate}</div>
        <div style="margin-top: 5px;">${pageText}</div>
      </div>
    </div>
  `;
}

