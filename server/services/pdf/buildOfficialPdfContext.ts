/**
 * ============================================================================
 * BUILD OFFICIAL PDF CONTEXT
 * ============================================================================
 * 
 * Unified context builder for ALL PDF generation across all modules
 * (Logistics, Finance, HR, Projects, Donor CRM, MEAL, etc.)
 * 
 * Combines:
 * ✅ Organization context (org/OU/branding)
 * ✅ Language & direction (EN/AR with RTL/LTR)
 * ✅ Common footer (organization, OU, date, page numbers)
 * ✅ Common metadata (generatedBy, generatedAt, documentId)
 * ✅ User context (who generated, when)
 * ✅ Styling & colors (branding)
 * 
 * USAGE:
 * ```typescript
 * const officialContext = await buildOfficialPdfContext({
 *   db,
 *   organizationId: ctx.scope.organizationId,
 *   operatingUnitId: ctx.scope.operatingUnitId,
 *   userId: ctx.user.id,
 *   language: 'en',
 *   documentType: 'purchase_request',
 *   documentId: 660007,
 * });
 * 
 * // Now use in PDF data:
 * const pdfData = {
 *   ...officialContext,
 *   // Add only module-specific fields
 *   prNumber: pr.prNumber,
 *   lineItems: [...],
 * };
 * ```
 * 
 * BENEFITS:
 * ✅ Ultra-clean module code (no boilerplate)
 * ✅ Consistent PDF styling across all modules
 * ✅ Automatic RTL/LTR and bilingual support
 * ✅ Built-in metadata tracking
 * ✅ Single source of truth for PDF context
 * ✅ Easy to extend with new fields
 * 
 * ============================================================================
 */

import { eq } from 'drizzle-orm';
import { users } from 'drizzle/schema';
import { TRPCError } from '@trpc/server';
import { PdfOrganizationContext, getPdfOrganizationContext } from './getPdfOrganizationContext';

/**
 * Official PDF Context - Complete context for all PDF generation
 */
export interface OfficialPdfContext extends PdfOrganizationContext {
  // ========== LANGUAGE & DIRECTION ==========
  language: 'en' | 'ar';
  direction: 'ltr' | 'rtl';
  isArabic: boolean;
  isRTL: boolean;

  // ========== USER CONTEXT ==========
  generatedBy: string; // User name who generated the PDF
  generatedByEmail: string;
  generatedAt: string; // ISO timestamp
  generatedAtFormatted: string; // Formatted date for display

  // ========== DOCUMENT METADATA ==========
  documentType: string; // 'purchase_request', 'bid_opening_minutes', etc.
  documentId: number;
  documentModule: string; // 'Logistics', 'Finance', 'HR', etc.

  // ========== FOOTER CONTEXT ==========
  footerOrganizationName: string;
  footerOperatingUnitName: string;
  footerText: string;
  footerDate: string;
  footerGeneratedBy: string;

  // ========== STYLING & COLORS ==========
  primaryColor: string;
  secondaryColor: string;
  accentColor: string;
  textColor: string;
  borderColor: string;
  backgroundColor: string;
  headerBackgroundColor: string;
  footerBackgroundColor: string;

  // ========== COMMON LABELS (BILINGUAL) ==========
  labels: {
    page: string; // 'Page' or 'صفحة'
    of: string; // 'of' or 'من'
    generatedBy: string; // 'Generated By' or 'تم الإنشاء بواسطة'
    generatedAt: string; // 'Generated At' or 'تم الإنشاء في'
    date: string; // 'Date' or 'التاريخ'
    organization: string; // 'Organization' or 'المنظمة'
    operatingUnit: string; // 'Operating Unit' or 'وحدة التشغيل'
    confidential: string; // 'Confidential' or 'سري'
    official: string; // 'Official Document' or 'وثيقة رسمية'
  };

  // ========== UTILITIES ==========
  textAlign: 'left' | 'right'; // Based on language
  alignClass: string; // 'text-left' or 'text-right'
  alignOpposite: 'left' | 'right'; // Opposite of textAlign
  marginStart: string; // 'ml' or 'mr' based on direction
  marginEnd: string; // 'mr' or 'ml' based on direction
  paddingStart: string; // 'pl' or 'pr' based on direction
  paddingEnd: string; // 'pr' or 'pl' based on direction
}

/**
 * Configuration for building official PDF context
 */
export interface BuildOfficialPdfContextConfig {
  db: any;
  organizationId: number;
  operatingUnitId: number;
  userId: number;
  language: 'en' | 'ar';
  documentType: string;
  documentId: number;
  documentModule?: string;
}

/**
 * Build complete official PDF context
 * 
 * @param config - Configuration object
 * @returns Complete official PDF context ready for use in any PDF generator
 * 
 * @throws TRPCError if organization, operating unit, or user not found
 */
export async function buildOfficialPdfContext(
  config: BuildOfficialPdfContextConfig
): Promise<OfficialPdfContext> {
  const {
    db,
    organizationId,
    operatingUnitId,
    userId,
    language,
    documentType,
    documentId,
    documentModule = 'General',
  } = config;

  console.log(
    `[Official PDF Context] Building context: module=${documentModule}, type=${documentType}, lang=${language}`
  );

  // ========== STEP 1: Get organization context ==========
  const orgContext = await getPdfOrganizationContext(db, organizationId, operatingUnitId);

  // ========== STEP 2: Get user information ==========
  const user = await db.query.users.findMany({
    where: eq(users.id, userId),
  });

  if (!user) {
    throw new TRPCError({
      code: 'NOT_FOUND',
      message: `User not found: ID=${userId}`,
    });
  }

  console.log(`[Official PDF Context] ✅ User found: ${user.name || user.email}`);

  // ========== STEP 3: Build language-specific labels ==========
  const labels = buildPdfLabels(language);

  // ========== STEP 4: Build styling context ==========
  const styling = buildPdfStyling(orgContext, language);

  // ========== STEP 5: Build utility classes ==========
  const utilities = buildPdfUtilities(language);

  // ========== STEP 6: Format timestamps ==========
  const now = new Date();
  const generatedAt = now.toISOString();
  const generatedAtFormatted = formatDateForPdf(now, language);

  // ========== STEP 7: Build footer context ==========
  const footerContext = buildFooterContext(orgContext, user, now, language);

  // ========== BUILD COMPLETE CONTEXT ==========
  const context: OfficialPdfContext = {
    // Spread organization context
    ...orgContext,

    // Language & Direction
    language,
    direction: language === 'ar' ? 'rtl' : 'ltr',
    isArabic: language === 'ar',
    isRTL: language === 'ar',

    // User Context
    generatedBy: user.name || user.email || 'Unknown User',
    generatedByEmail: user.email || '',
    generatedAt,
    generatedAtFormatted,

    // Document Metadata
    documentType,
    documentId,
    documentModule,

    // Footer Context
    ...footerContext,

    // Styling & Colors
    ...styling,

    // Labels
    labels,

    // Utilities
    ...utilities,
  };

  console.log(`[Official PDF Context] ✅ Official PDF context ready`);

  return context;
}

/**
 * Build bilingual labels for PDF
 */
function buildPdfLabels(language: 'en' | 'ar'): OfficialPdfContext['labels'] {
  if (language === 'ar') {
    return {
      page: 'صفحة',
      of: 'من',
      generatedBy: 'تم الإنشاء بواسطة',
      generatedAt: 'تم الإنشاء في',
      date: 'التاريخ',
      organization: 'المنظمة',
      operatingUnit: 'وحدة التشغيل',
      confidential: 'سري',
      official: 'وثيقة رسمية',
    };
  }

  return {
    page: 'Page',
    of: 'of',
    generatedBy: 'Generated By',
    generatedAt: 'Generated At',
    date: 'Date',
    organization: 'Organization',
    operatingUnit: 'Operating Unit',
    confidential: 'Confidential',
    official: 'Official Document',
  };
}

/**
 * Build styling context from branding
 */
function buildPdfStyling(
  orgContext: PdfOrganizationContext,
  language: 'en' | 'ar'
): Partial<OfficialPdfContext> {
  // Default colors
  const defaultPrimary = '#1e40af';
  const defaultSecondary = '#64748b';
  const defaultAccent = '#f59e0b';

  return {
    primaryColor: orgContext.brandingPrimaryColor || defaultPrimary,
    secondaryColor: orgContext.brandingSecondaryColor || defaultSecondary,
    accentColor: orgContext.brandingAccentColor || defaultAccent,
    textColor: '#1f2937',
    borderColor: '#e5e7eb',
    backgroundColor: '#ffffff',
    headerBackgroundColor: orgContext.brandingPrimaryColor || defaultPrimary,
    footerBackgroundColor: '#f9fafb',
  };
}

/**
 * Build utility classes and properties for RTL/LTR
 */
function buildPdfUtilities(language: 'en' | 'ar'): Partial<OfficialPdfContext> {
  const isRTL = language === 'ar';

  return {
    textAlign: isRTL ? 'right' : 'left',
    alignClass: isRTL ? 'text-right' : 'text-left',
    alignOpposite: isRTL ? 'left' : 'right',
    marginStart: isRTL ? 'mr' : 'ml',
    marginEnd: isRTL ? 'ml' : 'mr',
    paddingStart: isRTL ? 'pr' : 'pl',
    paddingEnd: isRTL ? 'pl' : 'pr',
  };
}

/**
 * Build footer context
 */
function buildFooterContext(
  orgContext: PdfOrganizationContext,
  user: any,
  date: Date,
  language: 'en' | 'ar'
): Partial<OfficialPdfContext> {
  const footerDate = formatDateForPdf(date, language);
  const generatedByText = user.name || user.email || 'System';

  return {
    footerOrganizationName: orgContext.organizationName || '',
    footerOperatingUnitName: orgContext.operatingUnitName || '',
    footerText: `${orgContext.organizationName} - ${orgContext.operatingUnitName}`,
    footerDate,
    footerGeneratedBy: generatedByText,
  };
}

/**
 * Format date for PDF display
 */
function formatDateForPdf(date: Date, language: 'en' | 'ar'): string {
  if (language === 'ar') {
    const formatter = new Intl.DateTimeFormat('ar-SA', {
      year: 'numeric',
      month: 'long',
      day: 'numeric',
      hour: '2-digit',
      minute: '2-digit',
    });
    return formatter.format(date);
  }

  const formatter = new Intl.DateTimeFormat('en-US', {
    year: 'numeric',
    month: 'short',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
  });
  return formatter.format(date);
}

