function kpiSpecFor(reportId: string): KpiSpec[] {
  if (KPI_REGISTRY[reportId]) return KPI_REGISTRY[reportId];
  if (["budget-vs-actual", "quarterly", "semiannual", "annual", "budget-performance", "budget-compliance"].includes(reportId))
    return KPI_REGISTRY._budget;
  if (["executive-summary", "exec-financial-summary", "commitments"].includes(reportId))
    return KPI_REGISTRY.monthly;
  return KPI_REGISTRY._budget;
}

