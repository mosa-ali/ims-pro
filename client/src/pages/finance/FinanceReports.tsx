// ─── Number helpers ─────────────────────────────────────────────────────────
const n = (v: unknown): number => {
  const x = Number(v ?? 0);
  return isNaN(x) ? 0 : x;
};
const money = (v: unknown, currency = "USD") => compactCurrency(n(v), currency);
const pct = (v: unknown) => `${n(v).toFixed(1)}%`;

