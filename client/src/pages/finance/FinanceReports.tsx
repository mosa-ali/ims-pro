// Generic analytical report
function GenericReportBody({ payload, currency, isLoading, lang }: BodyProps) {
  const t = T[lang];
  const columns = (payload?.columns ?? []) as { key: string; label: string; type?: string; align?: "start" | "end" }[];
  const rows = (payload?.rows ?? []) as any[];
  const chart = (payload?.chart ?? null) as { data: any[]; series: { key: string; label: string; color?: string }[] } | null;
  const totals = (payload?.totals ?? undefined) as Record<string, number> | undefined;

  const cols: TableColumn<any>[] = columns.map((c) => {
    const isNum = c.type === "currency" || c.type === "number" || c.type === "percent";
    return {
      key: c.key,
      label: c.label,
      align: c.align ?? (isNum ? "end" : "start"),
      ltr: isNum,
      sortable: true,
      render: (r: any) => {
        const v = r[c.key];
        if (c.type === "currency") return money(v, currency);
        if (c.type === "percent") return pct(v);
        if (c.type === "number") return <span>{n(v).toLocaleString()}</span>;
        if (c.type === "status") return <StatusPill status={String(v ?? "—")} />;
        return <span dir="auto">{v == null || v === "" ? "—" : String(v)}</span>;
      },
    };
  });

  const chartData = (chart?.data ?? []) as any[];
  const series = (chart?.series ?? []) as { key: string; label: string; color?: string }[];
  const chartTitle = series.map((s) => s.label).join(" · ") || t.category;

  return (
    <div className="grid gap-4">
      {series.length > 0 && (
        <ChartWrapper
          title={chartTitle} icon={<Activity className="h-4 w-4" />}
          isLoading={isLoading} isEmpty={chartData.length === 0} emptyMessage={t.noData} chartHeight="h-[300px]"
        >
          <ResponsiveContainer width="100%" height="100%">
            <BarChart data={chartData} margin={{ top: 8, right: 12, left: 0, bottom: 4 }}>
              <CartesianGrid stroke="#e7ebf0" vertical={false} />
              <XAxis dataKey="name" tick={{ fontSize: 11 }} />
              <YAxis tick={{ fontSize: 11 }} tickFormatter={(v) => compactCurrency(Number(v), currency)} />
              <Tooltip formatter={(v: number) => compactCurrency(Number(v), currency)} contentStyle={{ borderRadius: 8, border: "1px solid #e7ebf0", fontSize: 11 }} />
              {series.length > 1 && <Legend wrapperStyle={{ fontSize: 11 }} />}
              {series.map((s, i) => (
                <Bar key={s.key} dataKey={s.key} name={s.label}
                  fill={s.color ?? CHART_COLORS.series[i % CHART_COLORS.series.length]} radius={[4, 4, 0, 0]} />
              ))}
            </BarChart>
          </ResponsiveContainer>
        </ChartWrapper>
      )}

      {isLoading ? <FinanceCard><LoadingSkeleton variant="table" /></FinanceCard> :
        <ExecutiveTable columns={cols} data={rows} rowKey={(_, i) => i} emptyMessage={t.noData} searchable={rows.length > 10} />}

      {totals && Object.keys(totals).length > 0 && (
        <FinanceCard className="flex flex-wrap gap-x-8 gap-y-2">
          {Object.entries(totals).map(([k, v]) => (
            <div key={k} className="min-w-0">
              <span className="block text-[10px] font-bold uppercase tracking-wide text-slate-500">Total · {k}</span>
              <span className="block font-mono text-[14px] font-bold text-[#003b70]" dir="ltr">
                {Math.abs(n(v)) >= 1000 ? compactCurrency(n(v), currency) : n(v).toLocaleString()}
              </span>
            </div>
          ))}
        </FinanceCard>
      )}
    </div>
  );
}

interface BodyProps { payload: any; currency: string; isLoading: boolean; lang: Lang; }

// ─── Helpers ────────────────────────────────────────────────────────────────
function StatusPill({ status }: { status: string }) {
  const s = status.toLowerCase();
  const cls =
    s === "final" ? "bg-green-100 text-green-700"
    : s === "draft" ? "bg-amber-100 text-amber-700"
    : "bg-slate-100 text-slate-600";
  return <span className={`inline-block rounded px-2 py-0.5 text-[10px] font-bold capitalize ${cls}`}>{status}</span>;
}

