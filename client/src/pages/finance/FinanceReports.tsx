// ─── Report View Component ───────────────────────────────────────────────────
function ReportView({
  reportId,
  group,
  filterInput,
  reportQ,
  aiQ,
  historyQ,
  onExport,
  onRetry,
  lang,
  t,
}: {
  reportId: string;
  group: GroupKey;
  filterInput: any;
  reportQ: any;
  aiQ: any;
  historyQ: any;
  onExport: (format: "PDF" | "XLSX") => void;
  onRetry: () => void;
  lang: Lang;
  t: any;
}) {
  const reportTitleKey = CATALOGUE.flatMap((g) => g.reports).find(
    (r) => r.id === reportId
  )?.titleKey;
  const reportTitle = reportTitleKey ? t[reportTitleKey] : reportId;

  const envelope = reportQ.data;
  const payload = envelope?.data;
  const metadata = payload?.metadata;

  // ─── Handle Loading States ───────────────────────────────────────────────
  if (reportQ.isLoading) {
    return (
      <FinanceCard>
        <LoadingSkeleton />
      </FinanceCard>
    );
  }

  // ─── Handle Errors ──────────────────────────────────────────────────────
  if (reportQ.error) {
    return (
      <FinanceCard>
        <ErrorState message={reportQ.error.message || "Failed to load report"} onRetry={onRetry} />
      </FinanceCard>
    );
  }

  // ─── Handle Not Implemented ─────────────────────────────────────────────
  if (envelope?.status === "not_implemented") {
    return (
      <FinanceCard>
        <EmptyState title={t.notAvailableTitle} />
      </FinanceCard>
    );
  }

  // ─── Handle Report Errors ───────────────────────────────────────────────
  if (envelope?.status === "error") {
    return (
      <FinanceCard>
        <ErrorState message={envelope.message || "Report generation failed"} onRetry={onRetry} />
      </FinanceCard>
    );
  }

  // ─── Handle No Data ─────────────────────────────────────────────────────
  if (!envelope?.data) {
    return (
      <FinanceCard>
        <EmptyState title={t.noData} />
      </FinanceCard>
    );
  }

  const isLoading = reportQ.isLoading;
  const ok = envelope?.status === "ok" && !!payload;

  // ─── Render Report ──────────────────────────────────────────────────────
  return (
    <div className="flex flex-col gap-4" dir={lang === "ar" ? "rtl" : "ltr"}>
      {/* Report header */}
      <FinanceCard>
        <div className="flex flex-wrap items-start justify-between gap-3">
          <div className="min-w-0">
            <div className="mb-1 flex items-center gap-2">
              <span className="rounded bg-[#003b70]/10 px-2 py-0.5 text-[10px] font-bold uppercase tracking-widest text-[#003b70]">
                {t[CATALOGUE.find((g) => g.group === group)!.titleKey]}
              </span>
              {metadata?.status && <StatusPill status={metadata.status} />}
            </div>
            <h2 className="text-[18px] font-bold text-slate-900">{reportTitle}</h2>
          </div>
          {/* Actions */}
          <div className="flex flex-wrap items-center gap-2">
            <button
              onClick={() => window.print()}
              className="inline-flex items-center gap-1.5 rounded-lg border border-slate-300 bg-white px-3.5 py-2 text-[12px] font-semibold text-slate-700 transition-colors hover:bg-slate-50"
            >
              <Printer className="h-4 w-4" /> {t.print}
            </button>
            <button
              onClick={() => onExport("XLSX")}
              disabled={!ok}
              className="inline-flex items-center gap-1.5 rounded-lg border border-slate-300 bg-white px-3.5 py-2 text-[12px] font-semibold text-slate-700 transition-colors hover:bg-slate-50 disabled:opacity-50"
            >
              <Download className="h-4 w-4" /> {t.exportExcel}
            </button>
            <button
              onClick={() => onExport("PDF")}
              disabled={!ok}
              className="inline-flex items-center gap-1.5 rounded-lg bg-[#003b70] px-3.5 py-2 text-[12px] font-bold text-white transition-colors hover:bg-[#002a55] disabled:opacity-50"
            >
              <Download className="h-4 w-4" /> {t.exportPdf}
            </button>
          </div>
        </div>
      </FinanceCard>

      {/* Executive Summary */}
      <ExecutiveSummary
        reportId={reportId} payload={payload} currency={filterInput.currency}
        isLoading={isLoading} lang={lang}
      />

      {/* KPI strip */}
      <KpiStrip reportId={reportId} payload={payload} currency={filterInput.currency} isLoading={isLoading} lang={lang} />

      {/* Report Body */}
      <ReportBody reportId={reportId} payload={payload} currency={filterInput.currency} isLoading={isLoading} lang={lang} />

      {/* Financial Intelligence */}
      <FinanceCard noPadding>
        <WidgetHeader title={t.financialIntelligence} icon={<Bot className="h-4 w-4" />} />
        <div className="p-4">
          {aiQ.isLoading ? (
            <LoadingSkeleton variant="list" rows={3} />
          ) : aiQ.data?.insights?.length === 0 ? (
            <EmptyState
              title={t.financialIntelligence}
              message={aiQ.data?.explanation ?? t.noData}
              className="py-8"
            />
          ) : (
            <div className="space-y-2">
              {aiQ.data?.insights?.map((ins: any) => {
                const tone =
                  ins.severity === "critical" ? "border-red-100 bg-red-50 text-red-900"
                  : ins.severity === "warning" ? "border-amber-100 bg-amber-50 text-amber-900"
                  : "border-blue-100 bg-blue-50 text-blue-900";
                const badge =
                  ins.severity === "critical" ? "bg-red-200 text-red-900"
                  : ins.severity === "warning" ? "bg-amber-200 text-amber-900"
                  : "bg-blue-200 text-blue-900";
                return (
                  <div key={ins.id} className={`rounded border p-2.5 ${tone}`}>
                    <div className="flex items-center justify-between gap-2">
                      <p className="text-[12px] font-semibold">{ins.title}</p>
                      <span className={`shrink-0 rounded px-2 py-0.5 text-[10px] font-bold ${badge}`}>
                        {ins.category}
                      </span>
                    </div>
                    <p className="mt-0.5 text-[10px] opacity-80">{ins.detail}</p>
                  </div>
                );
              })}
            </div>
          )}
        </div>
      </FinanceCard>

      {/* Report History */}
      <FinanceCard noPadding>
        <WidgetHeader title={t.reportHistory} icon={<Clock className="h-4 w-4" />} />
        <div className="p-4">
          {historyQ.isLoading ? (
            <LoadingSkeleton variant="list" rows={3} />
          ) : historyQ.data?.length === 0 ? (
            <EmptyState title={t.reportHistory} message={t.noHistory} className="py-8" />
          ) : (
            <div className="space-y-1.5">
              {historyQ.data?.slice(0, 6).map((h: any) => (
                <div key={h.id} className="flex items-center justify-between rounded bg-slate-50 px-3 py-2 text-[11px]">
                  <span className="truncate font-medium text-slate-700">{h.fileName}</span>
                  <span className="flex items-center gap-3 text-slate-400">
                    <span>{new Date(h.generatedAt).toLocaleDateString()}</span>
                    <span className="font-mono uppercase">{h.mimeType?.includes("pdf") ? "PDF" : "XLSX"}</span>
                  </span>
                </div>
              ))}
            </div>
          )}
        </div>
      </FinanceCard>
    </div>
  );
}

// ─── Executive Summary ───────────────────────────────────────────────────────
function ExecutiveSummary({
  reportId, payload, currency, isLoading, lang,
}: { reportId: string; payload: any; currency: string; isLoading: boolean; lang: Lang }) {
  const t = T[lang];

  const summary = useMemo(() => {
    if (!payload) return null;
    const totalBudget =
      n(payload?.summary?.totalBudget) ||
      (Array.isArray(payload?.data) ? payload.data.reduce((s: number, r: any) => s + n(r.budgeted), 0) : 0);
    const totalActual =
      n(payload?.summary?.totalActual) || n(payload?.summary?.totalExpenses) ||
      (Array.isArray(payload?.data) ? payload.data.reduce((s: number, r: any) => s + n(r.actual), 0) : 0);
    const remaining = totalBudget - totalActual;
    const utilization = totalBudget > 0 ? (totalActual / totalBudget) * 100 : 0;
    const commitments = Array.isArray(payload?.sections?.commitments)
      ? payload.sections.commitments.reduce((s: number, r: any) => s + n(r.amount), 0)
      : 0;
    const variances = Array.isArray(payload?.data)
      ? payload.data.filter((r: any) => Math.abs(n(r.variancePercent)) > 10).length
      : 0;
    const health = utilization > 100 ? "critical" : utilization > 90 ? "watch" : "healthy";
    return { totalBudget, totalActual, remaining, utilization, commitments, variances, health };
  }, [payload]);

  if (isLoading) {
    return (
      <FinanceCard noPadding>
        <WidgetHeader title={t.executiveSummary} icon={<FileText className="h-4 w-4" />} />
        <div className="p-4"><LoadingSkeleton variant="text" rows={3} /></div>
      </FinanceCard>
    );
  }
  if (!summary || (!summary.totalBudget && !summary.totalActual)) return null;

  const healthColor =
    summary.health === "critical" ? FINANCE_COLORS.critical
    : summary.health === "watch" ? FINANCE_COLORS.warning
    : FINANCE_COLORS.success;
  const healthLabel =
    summary.health === "critical" ? t.critical : summary.health === "watch" ? t.watch : t.healthy;

  const cells: { label: string; value: string; color?: string }[] = [
    { label: t.budget, value: money(summary.totalBudget, currency) },
    { label: t.actual, value: money(summary.totalActual, currency) },
    { label: t.remaining, value: money(summary.remaining, currency), color: summary.remaining < 0 ? FINANCE_COLORS.critical : undefined },
    { label: t.utilization, value: pct(summary.utilization) },
    { label: t.commitments, value: money(summary.commitments, currency) },
    { label: t.financialHealth, value: healthLabel, color: healthColor },
  ];

  return (
    <FinanceCard noPadding>
      <WidgetHeader title={t.executiveSummary} icon={<FileText className="h-4 w-4" />} />
      <div className="grid grid-cols-2 gap-px bg-slate-100 sm:grid-cols-3 lg:grid-cols-6">
        {cells.map((c) => (
          <div key={c.label} className="bg-white p-3">
            <p className="text-[10px] font-bold uppercase tracking-wide text-slate-500">{c.label}</p>
            <p className="mt-1 font-mono text-[16px] font-bold" style={{ color: c.color ?? FINANCE_COLORS.navy }} dir="ltr">
              {c.value}
            </p>
          </div>
        ))}
      </div>
    </FinanceCard>
  );
}

// ─── KPI strip ──────────────────────────────────────────────────────────────
function KpiStrip({
  reportId, payload, currency, isLoading, lang,
}: { reportId: string; payload: any; currency: string; isLoading: boolean; lang: Lang }) {
  const t = T[lang];
  const specs = kpiSpecFor(reportId);

  if (isLoading) {
    return (
      <div className="grid grid-cols-2 gap-3 sm:grid-cols-3 lg:grid-cols-5">
        {Array.from({ length: specs.length }).map((_, i) => (
          <FinanceCard key={i} noPadding className="p-3"><LoadingSkeleton variant="kpi" /></FinanceCard>
        ))}
      </div>
    );
  }
  if (!payload) return null;
  if (Array.isArray(payload?.columns) && !payload?.summary) return null;

  return (
    <div className="grid grid-cols-2 gap-3 sm:grid-cols-3 lg:grid-cols-5">
      {specs.map((s) => (
        <FinanceKpiCard
          key={s.label}
          label={t[s.label]}
          value={s.value(payload, currency)}
          icon={s.icon}
          progressValue={s.progress?.(payload)}
        />
      ))}
    </div>
  );
}

// ─── Report body ────────────────────────────────────────────────────────────
function ReportBody({
  reportId, payload, currency, isLoading, lang,
}: { reportId: string; payload: any; currency: string; isLoading: boolean; lang: Lang }) {
  const t = T[lang];

  const isBudgetFamily =
    ["budget-vs-actual", "quarterly", "semiannual", "annual", "budget-performance", "budget-compliance", "project-performance"].includes(reportId);

  if (reportId === "cash-flow" || reportId === "cash-position") return <CashFlowBody payload={payload} currency={currency} isLoading={isLoading} lang={lang} />;
  if (reportId === "trial-balance" || reportId === "journal-validation")
    return <TrialBalanceBody payload={payload} currency={currency} isLoading={isLoading} lang={lang} />;
  if (reportId === "balance-sheet" || reportId === "org-summary" || reportId === "consolidated")
    return <BalanceSheetBody payload={payload} currency={currency} isLoading={isLoading} lang={lang} />;
  if (reportId === "income-statement" || reportId === "revenue-analysis")
    return <IncomeStatementBody payload={payload} currency={currency} isLoading={isLoading} lang={lang} />;
  if (isBudgetFamily) return <BudgetVsActualBody payload={payload} currency={currency} isLoading={isLoading} lang={lang} />;

  if (Array.isArray(payload?.columns)) return <GenericReportBody payload={payload} currency={currency} isLoading={isLoading} lang={lang} />;

  return <MonthlyBody payload={payload} currency={currency} isLoading={isLoading} lang={lang} />;
}

