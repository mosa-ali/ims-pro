// KPI Card
interface KPICardProps {
  title: string;
  value: string | number;
  icon: React.ReactNode;
  color: 'blue' | 'green' | 'purple' | 'orange' | 'cyan' | 'red';
  trend?: 'up' | 'down';
}

function KPICard({ title, value, icon, color, trend }: KPICardProps) {
  const colorClasses: Record<string, string> = {
    blue: 'bg-blue-50 text-blue-600',
    green: 'bg-green-50 text-green-600',
    purple: 'bg-purple-50 text-purple-600',
    orange: 'bg-orange-50 text-orange-600',
    cyan: 'bg-cyan-50 text-cyan-600',
    red: 'bg-red-50 text-red-600',
  };

  return (
    <Card className="relative overflow-hidden">
      <CardContent className="p-4">
        <div className="flex items-start justify-between">
          <div className="flex-1 text-start">
            <p className="text-xs font-medium text-gray-500 mb-1">{title}</p>
            <p className="text-xl font-bold text-gray-900 ltr-safe">{value}</p>
          </div>
          <div className={`p-2 rounded-lg ${colorClasses[color]}`}>
            {icon}
          </div>
        </div>
        {trend && (
          <div className="absolute bottom-2 end-2">
            {trend === 'up' ? (
              <TrendingUp className="w-3 h-3 text-green-500" />
            ) : (
              <TrendingDown className="w-3 h-3 text-red-500" />
            )}
          </div>
        )}
      </CardContent>
    </Card>
  );
}

// Executive Summary Tab
function ExecutiveSummaryTab({ data, t, language, isRTL }: { data: any; t: any; language: string; isRTL: boolean }) {
  const summary = data.executiveSummary;
  const healthList = data.projectHealthList;

  // Health distribution for pie chart
  const healthDistribution = useMemo(() => {
    const dist: Record<string, number> = {};
    for (const p of healthList) {
      dist[p.healthStatus] = (dist[p.healthStatus] || 0) + 1;
    }
    return Object.entries(dist).map(([name, value]) => ({
      name,
      value,
      color: HEALTH_COLORS[name] || '#6b7280',
    }));
  }, [healthList]);

  return (
    <>
      <div className="grid grid-cols-1 lg:grid-cols-2 gap-6">
        {/* Project Status Distribution */}
        <Card>
          <CardHeader>
            <CardTitle className="text-base">{t.projectHealth}</CardTitle>
          </CardHeader>
          <CardContent>
            {healthDistribution.length > 0 ? (
              <ResponsiveContainer width="100%" height={250}>
                <PieChart>
                  <Pie
                    data={healthDistribution}
                    cx="50%"
                    cy="50%"
                    labelLine={false}
                    label={(entry) => `${entry.name}: ${entry.value}`}
                    outerRadius={80}
                    fill="#8884d8"
                    dataKey="value"
                  >
                    {healthDistribution.map((entry, index) => (
                      <Cell key={`cell-${index}`} fill={entry.color} />
                    ))}
                  </Pie>
                  <Tooltip />
                </PieChart>
              </ResponsiveContainer>
            ) : (
              <EmptyChart message={t.noData} />
            )}
          </CardContent>
        </Card>

        {/* Key Metrics */}
        <Card>
          <CardHeader>
            <CardTitle className="text-base">{t.executiveSummary}</CardTitle>
          </CardHeader>
          <CardContent className="space-y-4">
            <MetricRow label={t.totalProjects} value={summary.totalProjects} />
            <MetricRow label={t.activeProjects} value={summary.activeProjects} />
            <MetricRow label={t.completedProjects} value={summary.completedProjects} />
            <MetricRow label={t.atRiskProjects} value={summary.atRiskProjects} color="orange" />
            <MetricRow label={t.criticalProjects} value={summary.criticalProjects} color="red" />
            <MetricRow label={t.expiringProjects} value={summary.expiringProjects} color="yellow" />
            <MetricRow label={t.overdueReports} value={summary.overdueReports} color="red" />
            <div className="pt-2 border-t">
              <MetricRow
                label={t.totalBudget}
                value={formatCurrency(summary.totalBudget, 'USD')}
              />
              <MetricRow
                label={t.totalSpent}
                value={formatCurrency(summary.totalSpent, 'USD')}
              />
            </div>
          </CardContent>
        </Card>
      </div>
    </>
  );
}

