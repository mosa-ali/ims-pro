/**
 * Get country configuration by name
 * Falls back to Yemen if country not found
 */
export function getCountryConfig(countryName: string | null | undefined) {
  if (!countryName) return COUNTRY_GOVERNORATE_COORDINATES['Yemen'];
  
  const normalized = countryName.toLowerCase().trim();
  
  // Try exact match first
  for (const [key, config] of Object.entries(COUNTRY_GOVERNORATE_COORDINATES)) {
    if (key.toLowerCase() === normalized) {
      return config;
    }
  }
  
  // Fallback to Yemen
  return COUNTRY_GOVERNORATE_COORDINATES['Yemen'];
}

/**
 * Get governorate coordinates within a country
 * Returns random coordinates within country bounds if governorate not found
 */
export function getGovernorateCoordinates(
  countryName: string | null | undefined,
  governorateName: string | null | undefined
): { lat: number; lng: number } {
  const countryConfig = getCountryConfig(countryName);
  
  if (!governorateName) {
    return countryConfig.center;
  }
  
  const normalized = governorateName.toLowerCase().trim();
  const coords = countryConfig.governorates[normalized];
  
  if (coords) {
    return coords;
  }
  
  // Fallback to country center with slight random offset
  return {
    lat: countryConfig.center.lat + (Math.random() - 0.5) * 2,
    lng: countryConfig.center.lng + (Math.random() - 0.5) * 2,
  };
}
